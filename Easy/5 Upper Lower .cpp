// IsLower

#include <bits/stdc++.h>
using namespace std;

int main(){

    char c;
    cin>>c;

    if(islower(c)){
        cout<<"Lowercase.";
    }

    else{
        cout<<"Uppercase.";
    }
    
    return 0;
}

