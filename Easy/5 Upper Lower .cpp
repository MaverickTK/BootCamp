// IsUpper

#include <bits/stdc++.h>
using namespace std;

int main(){

    char c;
    cin>>c;

    if(isupper(c)){
        cout<<"Uppercase.";
    }

    else{
        cout<<"Lowercase.";
    }
    
    return 0;
}


